package org.java_03;

public class single_inheritance {
	
	protected void tamilMovieList_2024() {
		System.out.println("Tamil Movie 2024");
		System.out.println("1.Ayalaan");
		System.out.println("2.Singapore Saloon");
		System.out.println("3.Lal Salaam");
	}

	public void tamilMovieList_2023(){
		System.out.println("Tamil Movie 2023");
		System.out.println("1.Jailer");
		System.out.println("2.Varisu");
		System.out.println("3.Good Night");
	}
	
	
	public static void main(String[] args) {
		single_inheritance movies=new single_inheritance();
		 movies.tamilMovieList_2024();
		 movies.tamilMovieList_2023();
 
	}
	}



